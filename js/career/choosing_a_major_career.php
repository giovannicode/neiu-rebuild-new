           <h3>Questions To Ask About A Major</h3>
           
           <p class="listing">Use your self-knowledge and the information you need to gather to answer these items.</p>
           <ul class="listingBullet">
               <li>What specific academic topics are studied?</li>
               <li>What learning approaches does this academic discipline use (science lab work, statistical/mathematical analysis, hands-on practice, abstract reasoning, etc.)? How do these fit your learning preferences?</li>
               <li>What coursework requirements need to be met?</li>
               <li>Which courses will be easier and more difficult for you?</li>
               <li>What particular skills are learned?</li>
               <li>What employers/industries hire for these skills?
                   What is the employment outlook/beginning salaries?</li>
               <li>Does this major garner any advantages to securing an internship opportunity that interests you?</li>
               <li>What types of careers do people with this major pursue?</li>
               <li>Is graduate school a requirement for progressing in these careers?</li>
           </ul>
           
