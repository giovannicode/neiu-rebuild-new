        <h3>How To Choose A Major</h3>
        
        <p>Choosing a Major vs. Choosing a Career - Which is first?</p>
        
        <p>
        There is no easy, one-size-fits all approach. Either requires information gathering and perspective to make a decision.
        </p>
        
        <p class="listing"> If you review the majors available at NEIU, you will</p>
        
        <ul class="listingBullet">
           <li>Select those academic departments that interest you.</li>
           <li>Explore the coursework by reviewing the departments websites and speaking with the faculty.</li>
           <li>From this information, limit your interests to two or three departments, and</li>
           <li>Register for courses in these areas.</li>
           <li>Confer with a career counselor/advisor.</li>
        </ul>
        
        <p>
        There is nothing wrong with being undecided about a major or career, but during college, individuals begin to explore 
        their options. This process continues after college, because workers must continuously adapt to increasingly dynamic 
        and fast-changing work environments. So, career planning and development is on-going. This allows, and often forces, 
        workers to have several careers. With increasing life-expectancy, you can work many years in each. So, if you like 
        more than one academic subject area, you might not want to limit yourself. Perhaps, you can make one subject a minor. 
        In any case, you should complement your main academic interest with other elective classes. Remember, to an employer, 
        the significance of your completion of a college degree means that you can receive training and instruction, communicate 
        both verbally and in writing, use analytical reasoning, and meet deadlines. Finishing the degree is the most important, 
        the particular major less so. The chose of a major does not dictate what you'll be doing for the rest of your life.
        </p>
        
