           <h3>Talking To Your Parents About Your Career Concerns</h3>
           
           <p>These suggestions may apply if you are attempting to resolve your career indecision.</p>
           <br>
           
           <h5>Keep the lines of communication open.</h5>
           <p>
           Arrange a time(s) to discuss your career decision with your parents. By doing so, you demonstrate that 
           you value their support and opinions and that you are taking the decision seriously. Communication is 
           two-way: Talk to them and listen to them. If they don't already, request that they do the same.
           </p>
           
           <br>
           
           <h5>Demonstrate to your parents that you are dealing with the situation in a mature manner.</h5>
           <p>
           Share the steps that you are taking to resolve your indecision or to outline your career 
           planning/preparation. Share your strategies by outlining them in a step-by-step, logical fashion.
           </p>
           
           <br>
           
           <h5>Be specific.</h5>
           <p>
           Parents may react unfavorably if you are keeping them in the dark. So, deal with specifics. 
           For example, if you are receiving career counseling, describe what has been discussed (e.g.-
           work values, interest themes, results of assessments, etc.) You need not reveal all that has 
           been covered in counseling, only those items that you are comfortable revealing and enough 
           to show the comprehensiveness of the approach.
           </p>
           
           <br>
           
           <h5>Present the facts.</h5>
           <p>
           Along with the item above, present factual information is helpful to update your parent's 
           perspective if it is outdated or inaccurate. Examples: Starting salaries, company hiring 
           practices, or employment projections predicted by the Bureau of Labor Statistics.
           </p>
           
           <br>
           
           <h5>Supplement your decision with available printed material.</h5>
           <p>
           Facts speak for themselves, and the paper they're printed on paints the picture. Use 
           assessment result reports, career books from the library, print-outs from career 
           websites, course catalog descriptions, and handouts from Career Services.
           </p>
           
           <br>
           
           <h5>Be assertive.</h5>
           <p>
           Say what's on your mind and express your feelings and opinions honestly and openly. Afford your parents the same courtesies.
           </p>
             
          
        
        
        
