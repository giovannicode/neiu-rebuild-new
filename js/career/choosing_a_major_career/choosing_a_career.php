            <h3>How to Choose a Career</h3>
        
        <br>
        
        <h4>Career Planning Guide</h4>
        <p>
        The Career Planning Guide allows students to understand the steps to successfully launch into a post-college career. 
        Along with a set of goals for each year of college, the guide lists four career management functions that 
        are necessary to successfully self-manage one's career. (1) Career exploration and self-discovery is an 
        introspective process to learn about oneself and how to apply one's attributes and interests to the world-of-work. 
        (2) Communication tools such as effective resumes and interviewing skills are basic elements of a job search. 
        (3) A successful strategy for identifying opportunities takes the job search to an operationalized level. (4) 
        Effective and prolific networking finds opportunities that are not formally advertised. <a target="_blank" href="/services/career/career/files/CareerPlanningGuide.pdf">Learn more...</a>
        
        </p>
        
        <h4>Career Planning Myths</h4>
        
        <p class="question">An interest inventory will tell me what I should do.</p>
        <ul class="questionBullet">
           <li>Interest inventories are good at measuring interests. However, a student also needs to 
           consider abilities, values, experiences, and practical considerations (expenses, opportunities, 
           family issues, etc.) An interest inventory is rarely sufficient.</li>
        </ul>
        
        <p class="question">Indecision is abnormal. "There's something wrong with me if I'm undecided."</p>
        <ul class="questionBullet">
           <li>Indecision is normal. Everybody has a different path and takes different amounts of time to 
           make career decisions. Indecision is only really a problem when you choose to do nothing about it. 
           If you are engaging in career exploratory activities, you are on the right track.</li>
        </ul>
        
        <p class="question">I need to decide today.</p>
        <ul class="questionBullet">
           <li>This is the myth perpetuated by those who think that career decision-making is an event 
           rather than a process. Making a decision prematurely, or based on inaccurate or insufficient 
           information, is invariably a mistake. Even if it means changing your academic plans, give 
           yourself time to make an intelligent and unrushed career plan.</li>
        </ul>
        
        <p class="question">Once I choose a career I'm stuck for life.</p>
        <ul class="questionBullet">
           <li>The average adult doesn't settle on a career until their 30's. Your interests, 
           values, abilities and aspirations may be different ages 20, 30, 40 and beyond. If so, 
           you change or modify your career accordingly. Changing careers is only a problem if you 
           think it is. Because of our society's dynamism, you can expect to have several careers.</li>
        </ul>
        
        <p class="question">My career choice must please my parents, friends, neighbors, etc.</p>
        <ul class="questionBullet">
           <li>The approval of others is nice, but it is not essential to your career well-being. 
           If others care about you, they'll probably be delighted if your career brings you happiness 
           and fulfillment. It's unfortunate if others aren't pleased with what you're doing, but it's not the end of the world.
            </li>
        </ul>
        
        <br/>

        
        <h4>What Can You Do To Help Choose And Prepare For A Career?</h4>
        
        <p class="listing">For more details on these activities, drop by Career Advising.</p>
        
        <ul class="listingBullet">
           <li>Take on "trial and error" part-time jobs.</li>
           <li>Read about careers. Use O*Net to find information on specific occupational titles.</li>
           <li>Learn about careers by talking to parents, friends, professors, academic advisors, community leaders, etc.</li>
           <li>Participate in Career Advising workshops.</li>
           <li>List your major concerns and fears about college life and discuss with a friend, parent, professor or counselor.</li>
           <li>Thoroughly examine the NEIU course catalog.</li>
           <li>Participate in study skills enhancement workshops for assistance with test-taking, test anxiety, and time management.</li>
           <li>Join a social service or community organization as a volunteer.</li>
           <li>Join a student organization.</li>
           <li>Consider study abroad.</li>
        </ul>
        
