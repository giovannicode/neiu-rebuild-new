         <h5>What is the Student Conduct Hearing Panel?</h5>
<p><b>The Student Conduct Hearing Panel (SCHiP) is the conduct review board composed of student peers for the campus community at Northeastern Illinois University. SCHiP is responsible for hearing conduct cases involving potential Student Code of Conduct violations.</b></p>

<p>The primary purpose of SCHiP is to educate students about campus policies and regulations while encouraging them to consider what it means to be responsible members of the community and be accountable for their actions. SCHiP members determine whether or not a violation of campus policies and procedures has occurred and appropriate sanctions if they determine it has occurred. </p>

<p>SCHiP members are aware of, and sensitive to, the environmental needs associated with Northeastern Illinois University such the importance of maintaining environments conducive to learning and the continued support of a diversity culture of students, faculty, and staff.  As a member of SCHiP, these students have an opportunity to have a voice and actively participate in maintaining Northeastern's health as a community of learning.</p><br/>

<h5>What can I gain from being a member of SCHiP?</h5>
<p>In today's world, employers are seeking college graduates who show high levels of leadership, communication, and critical thinking.  The Student Conduct Hearing Panel (SCHiP) will help build each member's capacity as student leaders in these areas, while also providing an outlet to work cooperatively with others and maintain a position of responsibility within the campus community. </p>

<p>In addition, a significant amount of research on college students has found that students who are involved on campus are more likely to be successful, achieve their educational and personal goals and objectives, and graduate from the institution. SCHiP members acquire substantial leadership experience, gain valuable interpersonal communication and critical thinking skills, and learn how to work together to apply the rules of due process while exercising sound judgment. They also develop knowledge of institutional policies and procedures and serve as role models for other students at the university.</p><br/>

<h5>What are the requirements of becoming a member of the panel?</h5>
<p>SCHiP needs responsible students who have excellent leadership and communication skills, who are committed to teamwork, and who have an interest in making a difference in their community. Students interested in applying should meet the following minimal requirements:</p>
<ul class="listingBullet">
<li>2.5 GPA at Northeastern Illinois University (or High School for First Year students)</li>
<li>Availability for hearings
<ul class="listingBullet2"><li>Most hearings will occur Monday-Friday from 8:30 am-4:30 pm, with a number of them being during Activity Hour either on Tuesday or Thursdays 3:05 pm-4:05 pm.</li>
<li>Be available during at least 1 Activity Hour (Tuesdays or Thursdays)</li></ul></li>
<li>Attendance at <b>MANDATORY</b> Student Conduct Hearing Panel interviews/trainings
<ul class="listingBullet2"><li>Interview date - June 26 at 2:00 pm-4:00 pm</li>
<li>Training date - July 13 at 9:00 am-4:00 pm</li></li></ul></ul>

