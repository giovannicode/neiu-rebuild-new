	     <h3>Minority Student Mentoring Program</h3>
	     
	     <p>
	     The first year of college can be interesting, adventurous and fun, yet challenging. The Minority Student
	      Mentoring program serves first year students who have little prior knowledge of the college experience. 
	      The program connects student on a personal level with university faculty or professional staff that cares 
	      about their academic and professional development and educational success.
	     </p>
	     
	     <p>
	     During the Fall semester, the Minority Student Mentoring Program (MSMP) matches mentors and mentees. 
	     The program kicks off with a fall welcome reception and ends with a holiday gathering. Throughout the year, 
	     mentors and mentees meet individually. The Partners for Success: Minority Student Mentoring Program is 
	     another way partnerships grow and have a lasting impact on a student's overall retention and success.
	     </p>
	     
