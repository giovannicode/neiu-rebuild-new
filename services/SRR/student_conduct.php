        <h3>Student Judicial Affairs</h3>
        
        <br/>
        
        <h4>Philosophy of University Policies for Judicial Affairs</h4>
        
        <p>
        Northeastern Illinois University (NEIU) is a community of learning where students are held 
        accountable for their actions. As individuals they enjoy basic rights, as members of a community 
        they also accept certain responsibilities. NEIU is a place where the free exchange of ideas is valued and where every person is important.
        </p>
        
        <p>
        In an effort to create an environment where the freedom to teach and learn are inseparable facets of 
        academic freedom, and to promote health and safety, NEIU has set forth policies that govern student 
        behavior. These policies set standards which NEIU encourages students to rise above and help create a community atmosphere of shared respect.
        </p>
        
        <p>
        The University's Judicial System was designed to address students' behavior that causes or threatens to 
        cause harm to another student, other members of the university community, or property, or creates a 
        pattern of extreme disruption, or indicates an extreme inability to cope with their own needs. Any such 
        behavior will violate the student conduct code and will be addressed through the student judicial system. 
        For example, a student who threatens another individual with immediate physical harm has violated the 
        abusive conduct policy and is subject to judicial action. Any behavior that is discriminating and/or 
        otherwise hurtful towards members of the community is addressed through judicial affairs and 
        educational interventions. NEIU is committed to using every available educational tool to stop 
        these behaviors in this community. University officials' behavior that causes or threatens to cause harm 
        to a student(s), other members of the university community, or property, or creates a pattern of extreme 
        disruption, or indicates an extreme inability to cope with their own needs will be addressed through 
        the university's Affirmative Action Office, not judicial affairs.
        </p>
        
        <p>
        The University Judicial System affects and applies to all currently enrolled students and to all registered 
        student organizations. Its major purpose is to help create a fair, just, and disciplined university community. 
        The system is based on the assumption that students and groups have the capacity to assume responsibility 
        for their own behavior and the university has the authority to establish an internal structure for the 
        enforcement of its policies and procedures that students have agreed to accept and follow by enrolling in the university.
        </p>
        
        <p>
        The University Judicial System is an educational tool with two main objectives: to hold students 
        accountable for unacceptable behavior and to modify those behaviors deemed unacceptable by the university. 
        The system strives to address unacceptable behavior in a manner that informs and guides students towards a 
        greater sense of personal responsibility and towards more mature and ethical standards.
        </p>
        
        <p>
        All students are responsible for understanding the Judicial Information and Procedures of the University Student Conduct Code. <a target="_blank" href="/services/SRR/files/conductCode.pdf">Read more...</a>
        </p>
        
