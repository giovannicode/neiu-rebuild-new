        <p class="question">What is academic advising?</p>
        <ul class="questionBullet">
           <li>
           The purpose of academic advising is to help you develop an educational plan and eventually 
           reach your specific educational goals. This process also involves helping you understand the 
           objectives and requirements of the bachelor's degree. You will find that helping you connect 
           your academic work with your personal passions is another goal. Your advisor is a valuable 
           resource for selecting courses, finding out about the requirements for various majors, helping 
           you understand general education requirements, and referring you to others for assistance when necessary.
           </li>
        </ul>
        
        <p class="question"> When should you meet with an advisor?</p>
        <ul class="questionBullet">
           <li>You should meet with your advisor whenever you have questions about your academic choices or your educational plan or need help with understanding the requirements of your major or degree.</li>
        </ul>
        
        <p class="listing"> An academic advisor is someone who can:</p>
        <ul class="listingBullet">
           <li>Help you plan an educational program based upon your interests, talents, and future goals.</li>
           <li>Answer your questions about taking summer classes.</li>
           <li>Suggest options if you do not like your current major.</li>
           <li>Inform you about various college or university policies and procedures.</li>
           <li>Help you decide whether or not to drop or add a course.</li>
           <li>Explain your General Education Requirements.</li>
           <li>Suggest ways you can correct an academic deficiency.</li>
           <li>Talk about next terms schedule.</li>
           <li>Refer you to a personal or financial counselor.</li>
           <li>Review your selection of classes to make certain that they are appropriate choices leading toward your particular degree or major.</li>
        </ul>
        
        <p class="listing">An academic advisor cannot:</p>
        <ul class="listingBullet">
           <li>Make decisions for you.</li>
           <li>Choose an appropriate major for you.</li>
           <li>Tell you which classes or professors are easy.</li>
           <li>Serve as a personal or financial counselor.</li>
           <li>Solve your problems for you.</li>
           <li>Give academic information about you to other parties without your written permission.</li>
        </ul>
        
