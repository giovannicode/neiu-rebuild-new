        <p>
        Thank you for partnering with our office in promoting the career development of your student.
        </p>
        
        <p>
        We value partnerships with parents to promote career development and to develop success in students. 
        We provide career counseling and resources to students who are unsure about a major to pursue. Our 
        office encourages students to gain real world experience during college that is related to their 
        career goals. We also continue to work with students as they become alumni. All of our services 
        are equally available to students and alumni.
        </p>
        
        <p>
        In order to assist you with understanding the services we provide, we have put together a list of frequently asked questions by parents.
        </p>
        
        <hr class="mainContentDivider">
        
        <h3>Frequently Asked Questions</h3>
        
        <p>Please review the questions below which are frequently asked by parents.</p>
        
        <p class="question">What resources are available on NEIU Career Advising's website?</p>
        <ul class="questionBullet">
          <li>Use the information on Finding a <a href="/services/career/finding_a_job_internship/index.php">Job/Internship</a> to learn about websites for job search communication, strategy, networking, and career exploration.</li>
       </ul>
       
       
       <p class="question">At what stage in my student's college career should they begin thinking about career planning?</p>
       <ul class="questionBullet">   
          <li>Freshmen should be engaged in career planning from their first semester. They can attend
              career workshops and begin a relationship with our staff. Through their coursework, they should evaluate their 
              academic abilities, interests, and preferred learning approaches. They should also explore their interests 
              and values through available assessments. Our online MyPlan service allows students to see how college majors 
              relate to specific occupations. Freshmen should also join clubs and student organizations to develop their 
              interests and leadership skills. Use the Career Planning Guide to see a four-year progression of career 
              development tasks.</li>
       </ul>
              
           <p class="question">How important is the selection of a major to the ability to enter a particular occupation?</p>
           <ul class="questionBullet">
              <li>Most occupations do not require a specific major to enter, but of course, a depressed job market 
              has an impact by increasing the competition for internships and jobs. Primarily, students should 
              explore career options that suit their strengths, values, and interests, and learn more about 
              the academic subjects they enjoy most.</li>
           </ul>
           
           <p class="question">What is Career Counseling?</p>
           <ul class="questionBullet">
              <li>Career Counseling focuses on enhancing one's awareness of his or her interests, personality, 
              values and strengths and increasing their knowledge of occupational information. A career 
              counselor facilitates the process, creating a focus on evaluating alternatives.</li>
           </ul>
           
           <p class="question">How has job searching changed in the last 20 years?</p>
           <ul class="questionBullet">
              <li>The world of work has changed drastically. Individuals will not only have several jobs, but 
              several careers over their working life. Along with the changes in the job market, there are 
              new sources for job searching. Career Advising keeps abreast of these changes. In workshops 
              and individual sessions with students, our staff teaches new skills 
              and adaptability to these changes.</li>
           </ul>
           
           <p class="question">How do I encourage my student to take control of their career?</p>
           <ul class="questionBullet">
              <li>Highlight any of your family members and friends who are making a career transition. 
              People in these circumstances are portraying personal growth and might serve as a 
              role model. In addition, refer your student to Career Advising to utilize our services.</li>
           </ul>
           
           <p class="question">How do I share my experiences in the workforce?</p>
           <ul class="questionBullet">
              <li>Your stories about the workplace give context to the career planning skills 
              being taught to students, though many things about career development and job 
              searching have changed. Career Advising can offer students a formal 
              understanding of career decision-making.</li>
           </ul>
           
           <p class="question">What is the role of internships and summer jobs in career decision-making?</p>
           <ul class="questionBullet">
              <li>Real world experience is one of the best ways to build the student's perception of their 
              fit in various occupations. Encourage your student to take opportunities that relate to 
              their interests. These experiences can develop their strengths and create more ideas for 
              career options. The first step is to create an account on our online job board, <a target="_blank" href="http://www.collegecentral.com/neiu/">CCN</a>, and 
              <a href="http://www.linkedin.com/groups/Northeastern-Illinois-University-Career-Services-3501861">listservs</a>.</li>
           </ul>
           
           <p class="question">How can I assist Career Advising?</p>
           <ul class="questionBullet">
              <li>By occasionally browsing our <a href="/services/career/job_fairs_workshops.php">Job Fairs, Workshops, and Events</a> page, you can stay current on 
              activities that might be missed by your student. These items can be easily cut-and-pasted to an 
              email for a quick 'heads-up' to your student. If you have specific industry experience, you 
              might be a valuable source of information.</li>
           </ul>
           
           <p class="question">What if my student is interested in graduate or professional school?</p>
           <ul class="questionBullet">
              <li>Decisions about graduate school are as much about career decision-making as they are about 
              attaining a higher credential. While attending graduate school can open up many doors, it 
              can also burden your student with substantial loans. Some professional degrees, once seen 
              as a winning lottery ticket, are no longer a sure thing. The Legal profession is one example. 
              Graduate school should not be attended simply because of external factors such as waiting out a 
              bad job market, as a result of a lay-off, or because the student is undecided about their future.</li>
           </ul>
           
           <p class="question">I want my student to gain leadership experience. How do they show that to employers?</p>
           <ul class="questionBullet">
              <li>Students should be involved with student organizations as early as their freshman year. 
              Organizing activities and events will sharpen leadership skills and teamwork. Taking on 
              projects such as submittals to professional conferences and poster board sessions 
              shows initiative. At the same time, students shouldn't overstretch themselves 
              just to be members of clubs. They should carefully select opportunities that allow 
              them to deeply invest their efforts. See <a target="_blank" href="http://www.neiu.edu/~sld/">Student Leadership Development</a>.</li>
           </ul>
           
           <p class="question">How useful is a double major?</p>
           <ul class="questionBullet">
              <li>A double major for its own sake provides little advantage to a student as a job seeker, 
              unless the specific combination provides value to an employer. For example, a degree in 
              English and Information Technology might be a strong preparation for becoming a 
              Technical Writer. Otherwise, a double major simply provides a stronger academic 
              preparation from college. Usually, pursuing a double major will entail an extra year of college.</li>
           </ul>
           
           <p class="question">	I want my student to graduate early. How is this perceived by employers?</p>
           <ul class="questionBullet">
              <li>Graduating early does not offer a particular advantage to students, and taking on extra 
              classes often comes with a trade-off. By not performing summer jobs/internships related 
              to a professional direction or by forgoing leadership roles in student organizations, 
              students don't distinguish themselves to employers.</li>
           </ul>
           
           <p class="question">How important is GPA to employers?</p>
           <ul class="questionBullet">
              <li>The importance of a GPA differs by major. An accounting major has more standardized 
              coursework than other majors. Therefore, the GPA is a more reliable benchmark of ability. 
              Other majors, particularly liberal arts majors, are more unstructured or variable between 
              colleges. So, a GPA provides a wider guideline for an employer and might be less significant.</li>
           </ul>
           
           <p class="question">How do I listen to my student?</p>
           <p>
           There are several formal ways to engage in a discussion and listen to your student about careers.
           </p>
           
           <ul class="questionBullet">
              <li>Arrange a time. Allow some preparation for a talk.</li>
              <li>Understand the key concepts of interests, work values, and skills. Educate yourself in the terminology as it relates to career development.</li>
              <li>Use printed material. This will center the discussion around impartial sources of information.</li>
              <li>Stick to facts and use detached observation. Use Internet resources like MyPlan. If you mention more subjective matters that constitute an opinion, detach yourself from its consequences for your family.</li>
              <li>Respect your student's opinion, but ask them to do the research to support it.</li>
           </ul>
           
           <p>
           Realize that your student will develop at their own pace. In the meantime, learn what is important to them.
           </p>
           
           <p class="note">
           Still have more questions or concerns? Please contact our <a href="/services/career/about_us.php">staff</a>.
           </p>
        
         
