      <h5>Reasons why accommodations could be denied:</h5><br/>
         <ul class="listingBullet">
                <li>If making the accommodation means making a  substantial change in an essential nature of a program or element of the  curriculum;</li>
  <li>If it poses an undue financial or  administrative burden and</li>
  <li>If they create a direct threat to the  health or safety of others.</li>
  <li>Accommodations do not transfer from one place to another. Each request is case-by-case and unique to each environment (i.e., the level of support in high school is vastly different from that in a higher education environment; the level of support from one higher education environment to another can also vary widely.)</li>
              
            
         </ul>
      
      
