         <p><b>To obtain eligibility to receive reasonable accommodations through Student Disability Services (SDS), students will need to follow the Accommodation Process outlined below. There are 4 steps to receive accommodations at Northeastern:</b>
         </p>
         
         <h4>Step 1</h4>
         
         <br/>
         
         <h5>Understand the Americans with Disability Act/Section 504:</h5><br/>
         
         <p>
         <u>The Rehabilitation Act of 1973, Section 504 / The Americans with Disabilities Act of 1990 (ADA) and amendments: </u><br/><br/>
This legislation applies to the opportunities that must be available to students choosing to attend an institution of higher education. Under ADA, students pursuing a post-secondary education are responsible for documenting and requesting accommodations for their specific needs. Students are responsible for identifying themselves as students who have a disability. The student must provide the University with appropriate documentation regarding their disability and recommended accommodations. 
         </p>
         
         <p>
         Students are responsible for requesting specific academic adjustments or accommodations according to their documented needs. Eligibility for reasonable accommodations in post-secondary institutions is driven by the federal definition of disability as a physical or mental impairment that substantially limits or restricts the conditions, manner, or duration under which an average person in the general population can perform a major life activity, such as walking, seeing, hearing, speaking, breathing, learning, working, or taking care of oneself. In a university setting, students must advocate for their own academic needs.
         </p>
         
         <br/>
         
         <h4>Step 2</h4>
         
         <h5>Students will need to print and complete the following forms:</h5><br/>
           <ul class="listingBullet">
			  <li><a target="_blank" href="/services/SDS/files/AccommReq.pdf">Student  Request for Accommodations Form</a></li>
			  <li><a target="_blank" href="/services/SDS/files/AuthRelease.pdf">Authorization  for Release of Information Form</a></li>
			  <li><a target="_blank" href="/services/SDS/files/ProviderReport.pdf">Provider  Report for Accommodation Request</a></li>
			</ul>
			
	     <h5>Understanding the forms:</h5><br/>
	     <ul class="listingBullet">
	        <li><a href="http://www.neiu.edu/~saffairs/SDS/Files/AccommReq.pdf">Student  Request Form</a> is for the student to fill out, letting SDS know what you are asking  for, what you've used in the past, and how you describe your disability. </li>
	        <li><a href="http://www.neiu.edu/~saffairs/SDS/Files/AuthRelease.pdf">Authorization  for Release of Information Form</a> is mandatory for all students seeking services  with SDS. This enables us to read your information and share it with <u>key</u> parties. Key parties are those that <u>need</u> to know what your  accommodations may be in order for your 
	             accommodations to be implemented  effectively. Key parties are trained in FERPA and other privacy mandates.  Confidentially is respected at all times</li>
	        
	        <li><a href="http://www.neiu.edu/~saffairs/SDS/Files/ProviderReport.pdf">Provider  Report for Accommodation Request</a> must be given to your clinician.
		              <ul class="listingBullet2">
					    <li>If  you are seeking accommodations for either a learning disability, ADHD, or other  psychological disabilities, the form must be completed by a licensed  psychologist/psychiatrist.</li>
					    <li> If you are seeking accommodations for a  physical disability (deaf/hard of hearing, low vision, or bone/muscle/neurological  disabilities) the form can be filled out by your family doctor. </li>
					    <li>If  you do not have a clinician SDS will supply a list of qualified clinicians (not  a list of recommendations). </li>
					  </ul>
	              </li>
	       
	           <li>Any  other supporting documentation will need to be submitted with the previous 3  forms. These could include:
					  <ul class="listingBullet2">
					    <li>IEP/504  paperwork</li>
					    <li>A  letter of approval for accommodations from the College Board</li>
					    <li>A  letter from a previous institution outlining the accommodations for which you were  approved.</li>
					  </ul class="listingBullet2">
	          </li>
	          
	            <li>All  documentation must be <u>no older than 3 years</u> prior to the date of the accommodation  request. 
					  <ul class="listingBullet2">
					    <li>If  your documentation is outdated, you may be eligible to receive temporary  accommodations while obtaining the updated documentation.</li>
					  </ul>
				</li>
	          
