         <p class="para1">
      Students who are visually impaired and legally blind students may receive accommodation services through Student Disability Services (SDS). Different degrees of visual acuity may hinder a student's ability to perform at a level comparable to visual students. Students must have corrected vision no better than 20/70 for a diagnosis of a visual impairment. After a visual impairment is diagnosed as stable, vision may continue to deteriorate.</p>

<p>Students who are legally blind have less than 20/200 vision in the better eye or a very limited field of vision. Their vision may be blurred, cloudy, double, or spotted. Sometimes, peripheral vision or central vision may be impaired. Students experiencing any of these visual problems should contact us. Listed below are helpful hints for blind/vision impaired students to follow when services are needed.</p>
 <ul class="listingBullet">
   <li> Request services 1- 2 weeks before the semester starts or the first week of the semester.</li>

<li>Obtain reading list and syllabi from the instructor prior to the semester. SDS will convert materials to alternate formats.</li>

<li>Make arrangements with SDS to receive examination assistance, e.g., proctor, scribe, enlarged copies, etc.</li>
<li>Make every effort to arrive early to class so you can sit in the front row of each class.</li>
<li>Discuss with your instructor alternate assignments if a specific task is impossible for you to complete.</li>
</ul>
