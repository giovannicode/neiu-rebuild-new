         <h3 id="jumpinglink_learning">Learning Disabilities</h3>
            
<p>Students diagnosed with this disability may experience difficulty comprehending reading, writing, mathematic calculations, listening and speaking.  Since the disability is not discernable by looking at an individual, students with one or all of these processing deficits often complain how people may perceive them as stupid or lazy.  They are neither, but instead learn or process information differently.  NEIU does not have a separate learning disability program but provides "reasonable accommodations" in compliance with federal mandates.</p>
<p>Each psychological evaluation submitted to the center must be complete.  Each test report should be signed and dated by a psychologist, with the diagnoses and tests administered as part of the report.  Services provided to the student must also be clearly depicted in the evaluation provided by the student to the center.  Further, in order to receive accommodations, the psychological evaluation submitted by the student can not be any older than three years from the students' SDS self-identification date.  Listed below are helpful hints for students with learning disabilities to follow when services are needed:</p>
<ul class="listingBullet">
  <li>Academic modifications may be needed to facilitate learning; therefore, please be able to discuss your individual learning style, learning deficits and how to address them.</li>

  <li>If students experience difficulty in oral language please request written information, visual aids, and provide staff and faculty with feedback.</li>

  <li>Request assistance from SDS staff if you have difficulties with grammar and sentence structure</li>

  <li>Comprehension problems with mathematics should be discussed with SDS staff</li>

  <li>Contact the SDS office if you experience poor organization, time management, outlining, exam preparation, recalling and/or memorizing problems</li>

  <li>If you need the auxiliary aid support of class note takers, technological and adaptive equipment assistance, tape recorded lectures, extended time on exams,  taped books, tutors, and/or readers please contact your assigned Accessibility Center counselor.    </li>
  </ul>
<p>Accommodations for difficulties in study skills are provided by SDS, but difficulties in academic supports are provided by the NEIU Learning Support Center or TRIO Support Services, which are located in the Library, 4th Floor.</p>
          
