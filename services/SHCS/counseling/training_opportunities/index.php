          <h5>Mission and Philosophy of Training</h5>
          
          <p>
          In the context of the larger University, Counseling Services provides an environment rich with 
          opportunities for learning and deepening skills. In many ways more a community mental health center 
          than a traditional college counseling center, our office provides services for a community that is
           broadly diverse across all dimensions and that presents an equally broad range of clinical issues. 
           Postdoctoral fellows and predoctoral practicum students participate in all aspects of our 
           office's activities. We view clinical training as an advanced apprenticeship in a service 
           environment committed to ongoing learning, professional independence and reflection about practice.
          </p>
          
          <br>
          
          <h5>Characteristics of the student body and our clients</h5>
          
