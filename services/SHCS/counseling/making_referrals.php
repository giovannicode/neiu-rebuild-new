        <h5>When can a referral to a counselor help?</h5>
        
        <p>
        When someone you know shares difficulties that are beyond your ability to help, or when a student's 
        behavior suggests serious emotional problems, refer the student to Student Counseling Services. 
        </p>
        
        <p class="listing">Counselors can help people work through a wide variety of issues, including:</p>
        
        <ul class="listingBullet">
        <li>Depression and anxiety</li>
        <li>Suicidal thoughts or feelings</li>
        <li>Grief and loss</li>
        <li>Problems in current relationships</li>
        <li>Incest or sexual abuse</li>
        <li>Eating disorders</li>
        <li>Addictions</li>
        </ul>
        
        <p>
        Northeastern's Student Counseling Services provides individual, group and 
        marital/couple counseling for help with these issues. And referrals to outside providers 
        may also be made when the situation warrants an evaluation for medication or calls for 
        services best provided by others.
        </p>
        
        <br>
        
        <h5>How do I make a referral?</h5>
        
        <p>
        When someone confides in you, they are affirming that you have an important 
        relationship with them; this relationship is a powerful tool that you can use to encourage someone to seek help.
        </p>
        
        <p>
        Let the individual know that you are concerned about his or her welfare but that the problem is beyond your 
        field of expertise. Indicate that counseling may help him or her deal with the situation more effectively. 
        Finally, suggest an initial meeting with a counselor to see if it may be useful. You can't force someone to 
        seek help, but your expression of concern can be a powerful influence on the other's choice.
        </p>
        
        <br>
        
        <h5>How much should you help?</h5>
        
        <p>
        That depends... on the nature of the problem, the degree of crisis, and the ability of the person 
        to follow through. Sometimes simply giving someone a name to call is sufficient; at other times, 
        actually making a call to the counseling office while you are meeting with a student is effective. 
        If you aren't sure that the student will follow through, you may want to call and arrange a meeting 
        with a counselor, walking the student down to meet with the counselor for the first time. Your goal 
        is to ensure that the student and the counselor make contact. We'll take it from there.
        </p>
        
        <br>
        
        <h5>What about a crisis?</h5>
        
        <p class="listing"> In an emergency, don't hesitate to act. These are your first resources:</p>
        
        <ul class="listingBullet">
           <li>Potential danger, medical emergency or immediate disruption - Call University Police (ext. 5511). They will respond in any cases of immediate danger.</li>
           <li>Psychological or Emotional crisis - Call Student Counseling Services (ext. 4650). 
           We can provide counseling and assess the need for more extensive psychological services, making arrangements as necessary. 
           During normal semester hours, the office is open Mon-Thur from 9 am-6 pm, Fri 9 am-4 pm.</li>
           <li>Non-emergency medical situation - Call Student Health Services (ext. 5800). They can respond and assess the situation. 
           If there is need for more extensive assistance, they will make arrangements.</li>
           <li>Non-emergency Student Disruption - In a non-emergency situation, call Student Rights and Responsibilities (ext. 4610) for information about dealing with disruptive students.</li>
       </ul>

       
       <h5>What will I hear back?</h5>
       
       <p>
       Because the counseling process is confidential, we cannot discuss with you what someone shares with us.
        In fact, we can't even tell you whether the student has made contact with us without his or her permission. 
        We do, however, encourage students to let you know that they have followed through on your referral and made 
        contact with us. We may also ask the student if we can contact you for additional information. For more 
        information on the limits to confidentiality, consult our page <a href="/services/SHCS/counseling/about_counseling.php">About Counseling</a>.
