        <h3>Frequently Asked Questions About Counseling</h3>
        
        <br>
        
        <h5>How can I tell if counseling is right for me?</h5>
        
        <p>
        You may benefit from psychological counseling for a variety of reasons... For instance, you 
        may wish to find solutions to specific problems in your life, explore issues in your relationships, 
        better understand your feelings, or develop greater self-esteem. In these cases, working with a 
        trained psychologist or counselor may help. Nothing is off-limits to discuss; counselors have 
        worked with people who have experienced a tremendous variety of difficulties. They are trained 
        to listen, understand, and help you with the kinds of problems that you may not be able to resolve on your own.
        </p>
        
        <br>
        
        <h5>Am I unusual in feeling I might need counseling?</h5>
        
        <p>
        No, you're not. Many students seek counseling at distressing times in their lives. In fact, 
        Northeastern's Counseling Services provided over 3,000 counseling 
        sessions last year to Northeastern students. Counseling is a "normal" and growthful process 
        that aims to help you with problems you may not be able to resolve on your own.
        </p>
        
        <br>
        
        <h5>What problems can I explore?</h5>
        
        <p class="listing">In a counseling relationship, you can explore a variety of issues, including:</p>
           <ul class="listingBullet">
              <li>depression</li>
              <li>anxiety</li>
              <li>grief and loss</li>
              <li>eating disorders</li>
              <li>problems in current relationships</li>
              <li>incest or sexual abuse</li>
              <li>drinking, drug or other addictions</li>
              <li>many other issues...</li>
           </ul>
           
           <p>
           In cases where your needs extend beyond the range of our services, we will refer you to other qualified professionals in the community.
           </p>
           
           <br>
           
           <h5>What happens when I first meet with a counselor?</h5>
           
           <p>
           When you first call for an appointment, we will schedule an "intake" session, which we try to arrange within a week or so of your call although sometimes it 
           may take a bit longer. During this intake session, we will ask you about your needs and determine which services might be best for you--for instance we 
           might recommend individual, group or couple counseling, or perhaps other services. Sometimes, you may continue to work with the counselor you met during 
           this intake appointment, but in other cases you might be connected to a different counselor whose schedule or training better matched your needs.
           </p>
           
           <br>
           
           <h5>What's group counseling?</h5>
           
           <p>
           Individual counseling involves meeting with a counselor one-on-one to discuss your 
           problems; in group counseling, you meet with a counselor along with several other 
           students, and together explore areas of common concern. Group sessions are usually 
           scheduled once a week, and the combined experiences and support of other group members 
           can make this a very powerful form of helping. Discuss with a counselor which approach 
           is most appropriate to your needs. A Group Counseling FAQ is available <a href="/services/SHCS/counseling/groups_services.php">here</a>.
           </p>
           
           <br>
           
           <h5>What about confidentiality?</h5>
           
           <p>
           Counseling requires a private and confidential relationship in which you can feel safe 
           to talk about your concerns. Consequently, information you share with your counselor is treated as 
           confidential, according to professional ethics, University regulations, and state and federal law. 
           
           In general, we won't release information about you to others without your prior written consent, 
           and information that you share in counseling does not become part of your academic record. We do 
           not discuss information about you with your instructors or your supervisors, nor do we release 
           information about you to your family members. However, there may be times when you want us to contact 
           other counseling or medical providers; in that event, we require that you complete a written consent 
           form authorizing us to do so. In this way, you are in control of information about your counseling. 
           The only exceptions to this policy are those unusual circumstances when confidential information is 
           needed to protect someone's health or safety, for instance, if we feel you present a clear and imminent 
           risk of danger to yourself or someone else, or in cases of suspected child abuse, where disclosure is 
           mandated by law.
           </p>
           
           <br>
           
           <h5>Who is eligible? And are there session limits?</h5>
           
           <p>
           All currently registered Northeastern students are eligible to receive individual, couples, and 
           group counseling services. We typically provide short-term individual and couples counseling 
           (6-8 sessions). There may be some opportunities for a student to work with a counselor on a 
           longer-term basis. This decision will be based on the need for such services as well as the 
           student's and counselor's availability. Students are eligible to receive up to 50 individual 
           or couples sessions total. To receive couples counseling, only one member of the couple need 
           be a student. Group counseling services are offered with no session limits. Of course, 
           availability of services is dependent on the capacity of our counseling staff and the 
           suitability of our services to your needs.
           </p>
           
           <br>
           
           <h5>Do services cost?</h5>
           
           <p>
           No. Counseling services are free to all current Northeastern students, in accord with the limits 
           discussed above. When tests or inventories are used by our counselors to help you, they are also provided free of charge.
           </p>
           
           <br>
           
           <h5>How do I refer someone else for counseling?</h5>
           
           <p>
           Referring a friend, student or colleague to counseling can be an important way of helping. 
           Check out <a href="/services/SHCS/counseling/making_referrals.php">Making Referrals</a> for tips on how to refer someone to Counseling Services.
           </p>
           
           <br>
           
           <h5>Do you provide services for Faculty & Staff?</h5>
           
           <p>
           Northeastern's Counseling Services provides psychological consultation 
           and referral services to members of the faculty and staff. While always available to help 
           faculty or staff members understand the psychological aspects of their work with students, 
           we also help connect employees with Northeastern's Employee Assitance Program, their health 
           insurance resources or practitioners in the community. Check out <a href="/services/SHCS/counseling/faculty_info.php">Information for 
           Faculty & Staff</a> for details on securing counseling assistance for employees.
           </p>
           
