          <p>
          Northeastern's Counseling Services provides training opportunities for those pursuing careers 
          in Clinical Psychology. We offer both Postdoctoral Fellowships and Predoctoral Therapy Practicum placements.
          </p>
          
          <br>
          
          <h5>Mission and Philosophy of Training</h5>
          
          <p>
          In the context of the larger University, Counseling Services provides an environment rich with 
          opportunities for learning and deepening skills. In many ways more a community mental health center 
          than a traditional college counseling center, our office provides services for a community that is
           broadly diverse across all dimensions and that presents an equally broad range of clinical issues. 
           Postdoctoral fellows and predoctoral practicum students participate in all aspects of our 
           office's activities. We view clinical training as an advanced apprenticeship in a service 
           environment committed to ongoing learning, professional independence and reflection about practice.
          </p>
          
          <br>
          
          <h5>Characteristics of the student body and our clients</h5>
          
          <p>
          One of the most diverse universities in the Midwest, Northeastern has a population 
          that represents students and staff members from a wide range of ages, ethnic backgrounds, 
          socio-economic levels, gender identities, and cultural and religious groups. Over 60% of
           Northeastern's students are the first in their families to pursue a college education,
            and many are "non-traditional" students, coming from groups underrepresented in higher 
            education. Significant numbers of returning adults also compliment this diversity. 
            Because many of our students are in the process of a life transition, they experience 
            high levels of stress as they balance the often competing worlds of school, work, and home.
          </p>
          
