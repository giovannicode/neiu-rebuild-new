          <h3>About Counseling Services</h3>
          
          <p>
          Student Health and Counseling Services is a service unit within the Division of Student Affairs, 
          the area responsible for the many non-academic and co-curricular support needs of our students.
           Some of the other service units within the Division of Student Affairs include Career Services, 
           Campus Recreation and Student Leadership Development (formerly Student Activities), Tutoring, Advising, Disability Services, and 
           TRIO Support Services. Within this milieu, Counseling Services provides counseling services, outreach efforts 
           and crisis intervention to Northeastern students, along with consultation services for university faculty and administrative staff.
          </p>
          
          <br/>
          
          <h4> Services Provided</h4>
          
          <p>
          Counseling Services provides a range of outpatient clinical services to university 
          students and staff members. Primary clinical activities include individual, couple, 
          family and group psychotherapy, as well as outreach and consultation services.
          </p>
          
          <p>
          A short description follows:
          </p>
          
          <br>
          

         
              <h5>Individual Psychotherapy</h5>
              
              <p>
              Individual psychotherapy is the most common service modality. What characterizes individual treatment at Northeastern is the very 
              diverse client population, representing a wide variety of background characteristics and clinical presentations. Psychological services 
              include crisis intervention and both brief and longer-term treatment.</p>
              
              <br>
              
              <h5>Couples and Family Psychotherapy</h5>
              <p>Although individual therapy is predominant, Counseling Services also provides couples and family therapy. Whether clients are seen individually or in couples is 
              determined primarily by client need and the clinical judgement of the therapist. During recent years, approximately 5% of the office's sessions 
              involved couple or family treatment.</p>
              
              <br>
              
              <h5>Group Psychotherapy</h5>
              <p>All clinical staff are encouraged to help develop therapy, support and psychoeducational groups on campus. These groups frequently have a 
              specific focus to address the needs of target audiences, and are sometimes sponsored in collaboration with other service units. 
              Previous groups have included an ongoing psychotherapy process groups, as well as counseling groups focusing on specific target
               populations and issues, such as anxiety management, stress management, parenting skills, a women's group and a group for students of color.</p>
               
               <br>
               
