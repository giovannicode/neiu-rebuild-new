         <h3>Postdoctoral Fellowship</h3>
         
         <br/>
         
         <h4>About the Postdoctoral Experience</h4>
         
         <p>
         Northeastern's Counseling Services has a long history of involvement in 
         various levels of clinical training. Originally serving as a practicum site for masters-level training, the office 
         began to offer pre-doctoral internships to students in Clinical Psychology in 1994. After 10 years of training interns, our office moved to a postdoctoral training model in 2004.
         </p>
         
         <p>
         The focus of the postdoctoral experience involves the development of professional independence and interests while completing supervised hours to meet licensure requirements. This is 
         accomplished by joining the clinical team with full involvement in office activities, including:
         </p>
         
         <ul class="listingBullet">
         <li>Providing clinical services (individual, couple and group therapy)</li>
         <li>Supervising doctoral level trainees</li>
         <li>Interviewing and selecting doctoral level trainees</li>
         <li>Outreach and consultation to the university community</li>
         <li>Participating in case conferences</li>
         <li>Peer consultation</li>
         <li>Psychiatric case-consultation</li>
         </ul>
         
         <h4>Stipend and Benefits</h4>
         
         <p>
         The stipend for the postdoctoral year is $30,000. Additionally, postdoctoral fellows are provided 
         the complete State of Illinois employee benefit package, which includes health, dental and life insurance, 
         retirement programs and other features. Faculty-level library privileges and a university-based email 
         account are provided; access to the University's recreational facilities is also available.
         </p>
         
         <p>
         For additional information about these facilities, click <a target="_blank" href="http://library.neiu.edu/">here</a> for the University Library 
         and <a target="_blank" href="http://www.neiu.edu/campusrecreation">here</a> for the Campus Recreation and Athletic facilities.
         </p>
         
         <br/>
         
         <h4>Requirements</h4>
         
         <p>
         By the start date of the postdoctoral fellowship, candidates must have completed all requirements for their doctoral 
         degree in Clinical or Counseling Psychology. Previous experience in a college or university counseling center is desirable.
         </p>
         
