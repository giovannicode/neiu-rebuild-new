        <h3>Getting help for someone struggling with suicidal thoughts</h3>
        
        <p>
        Suicide is a national problem, but suicide is often preventable. Understanding Suicide 
        provides information about the warning signs of suicide, how these feelings might express 
        themselves in an academic setting, how people who are struggling with suicide feel, and how you can help.
        </p>
        
        
        
        <h4>Understanding Suicide</h4>
        
        <h5>How much of a problem is suicide, anyway?</h5>
                         <p class="note">Here are a few quick facts:</p>
                         
         <ul class="listingBullet">
            <li>Suicide is the 8th leading cause of death in the U.S., killing about 30,000 people every year</li>
            <li>Suicide is the 3rd leading cause of death for 15-24 year olds</li>
            <li>More people die from suicide than from accidents, homicides and war combined</li>
            <li>Every day, approximately 80 Americans take their own lives--one every 18 minutes--somebody attempts suicide in the U.S. every minute</li>
            <li>80% of college students who die by suicide are not receiving counseling services or known to be suicidal</li>
            <li>Suicide is often preventable</li>
         </ul>
         
         <h4>Helping someone struggling with suicidal thoughts</h4>
         
       
         
         <h5>Be aware of warning signs</h5>
         
         <p>
         Thoughts about death or suicide are common in depression and it is important to take them seriously. 
         Although it is not possible to predict suicide with absolute precision, there are signs that a person may be considering suicide. 
         </p>
         
         <p class="note">If you see or hear these warning signs, seek professional help now:</p>
         <ul class="listingBullet">
            <li>Expressing thoughts of harming oneself, writing about death or suicide, threatening to kill oneself</li>
            <li>No reason for living; no sense of purpose in life</li>
            <li>Anxiety, agitation, unable to sleep or sleeping all the time</li>
            <li>Feeling trapped--like there's no way out</li>
            <li>Hopelessness</li>
            <li>Withdrawing from friends, family and society</li>
            <li>Dramatic mood or behavioral changes</li>
            <li>Rage, uncontrolled anger, seeking revenge</li>
            <li>Increased alcohol or drug use</li>
            <li>Acting reckless or engaging in risky activities, seemingly without thinking; seeking access to firearms; stockpiling pills</li>
         </ul>
         
         
         
         <h5>What signs might you see in school?</h5>
         <p class="note">These are some of the things you might notice in a student or classmate that could indicate suicide might be a problem:</p>
             <ul class="listingBullet">
                <li>Sudden drop in grades or performance</li>
                <li>Not seeming to care any more about school</li>
                <li>Disappearing from class, clubs or regular activities</li>
                <li>Neglect of appearance or hygiene</li>
                <li>Increase in drinking or drug use</li>
                <li>Difficulty sleeping or lack of sleep</li>
                <li>Getting fired from a job or expelled from school</li>
                <li>Sudden improvement in mood following a depression</li>
             </ul>
             
             
             
             <h5>Be aware of feelings</h5>
             
             <p>
             Many people at some time in their lives think about completing suicide. Most 
             decide to live because they eventually come to realize that the crisis is 
             temporary and death is permanent. On the other hand, people having a crisis 
             sometimes perceive their dilemma as inescapable and feel an utter loss of control.</p>
             
             <p class="note">These are some of the feelings and thoughts they experience:</p>
             
             <ul class="listingBullet">
                <li>Can't stop the pain, or can't see a future without pain</li>
                <li>Can't think clearly or make decisions</li>
                <li>Can't see any way out</li>
                <li>Can't sleep, eat or work</li>
                <li>Can't seem to get control</li>
                <li>Can't get out of depression or make the sadness go away</li>
                <li>Can't see themselves as worthwhile</li>
             </ul>
             
             <p class="note">And these are some of the things a suicidal person might say or think:</p>
			 
             <ul class="listingBullet">
                 <li>&quot;Soon you won't have to worry about me&quot;</li>
                 <li>&quot;I won't be around much longer&quot;</li>
                 <li>&quot;I just want to die.&quot;</li>
                 <li>&quot;Who cares if I'm dead anyway?&quot;</li>
                 <li>&quot;My family would be better off without me&quot;</li>
             </ul>
             
          
             
             <h5>What to do?</h5>
             <p class="note">Here are some ways to be helpful to someone who is threatening suicide:</p>
             
             <ul class="listingBullet">
             <li>Be direct. Talk openly and matter-of-factly about suicide. You won't be putting ideas into his/her head; this is a myth.</li>
             <li>Be willing to listen. Allow the expressions of feelings. Accept the feelings. Being heard is very powerful.</li>
             <li>Be non-judgmental. Don't debate whether suicide is right or wrong, or whether feelings are good or bad. Don't lecture on the value of life.</li>
             <li>Get involved. Become available. Show interest and support. This helps prevent isolation and withdrawal.</li>
             <li>Don't dare him or her to do it.</li>
             <li>Don't act shocked. This will put distance between you.</li>
             <li>Offer hope that alternatives are available but do not offer glib reassurance.</li>
             <li>Don't be sworn to secrecy. Seek support from others.</li>
             <li>Get help from persons or agencies specializing in crisis intervention and suicide prevention.</li>
             <li>Take action. Remove means, such as guns or stockpiled pills, when it is safe to do so. Call the police if you believe a suicide is imminent.</li>
             </ul>        
        
