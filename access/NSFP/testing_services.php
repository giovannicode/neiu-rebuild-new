	        <h3>The English Placement Test</h3>
	        
	        <p>
	        The purpose of the English Placement Test (EPT) is to place students in appropriate English classes so they can not only satisfy the 
	        University's English composition requirement, but also so they can be prepared for their general education and major courses. 
	        </p>
	        
	        <br/>
	        
	        <h4>Who needs to take the English Placement Test?</h4>
	        
	        <p>
	        The English Placement Test (EPT) is given to all incoming freshmen and transfer students who have not successfully completed ENGL 101 or an 
	        equivalent course at their previous institution. The EPT is divided into two parts: a writing test and a reading test. 
	        </p>
	        
	        <p>
	        Not all students are required to take the reading portion of the EPT. Students who enter the university with an ACT reading score of 20 or higher, will be excused from taking the reading portion of the English Placement Test but they still need to take the writing portion.
	        </p>
	        
	        <p class="note">
	       If a student wants to retake the test, he or she will have to wait for one calendar year.
	        </p>
	        
