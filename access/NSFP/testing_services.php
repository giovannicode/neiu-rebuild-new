	        <h3>The Math Placement Test</h3>
	        
	        <p>
	        The Math Placement Test (MPT) evaluates your initial course readiness to be successful in mathematics at Northeastern 
	        Illinois University. The university does not allow students to register for any mathematics course without prior testing (MPT or ACT) or 
	        college-level coursework. If a student wants to retake the test then the student will have to wait for one calendar year.
	        </p>
	        
	        <br/>
	        
	        <h4>Freshmen</h4>
	        <p>
	        All entering freshman students are given two options for placement into a math course:  ACT Math score or the Math Placement Test (MPT).  Freshmen who submit ACT 
	        scores to the university can be placed directly into a math course solely based on their ACT math score.  They also have the option of taking the Math 
	        Placement Test (MPT), in which case the university will use the higher of the two scores for placement into a math course.  Since there is no penalty to taking the
MPT, freshmen are strongly encouraged to do so, particularly if they took a math course in their senior year of high school. 
	        </p>
	        
	        <p class="listing">
	        The Math Placement Test is designed as Basic and Advanced.
	        </p>
	        <ul class="listingBullet">
	           <li>Freshmen with Math ACT scores of 21 or less are given the Basic MPT and 22 or higher are given the Advanced MPT.  </li>
	           <li>Freshmen who do not submit ACT scores must take the Basic MPT.</li>
	           <li>Freshmen who receive a score of '3' or higher on the AP Calculus test are not required to take the MPT. </li>
	         </ul>
	         
	         </br>
	         
	         <h4>Transfers</h4>
	         
	         <p>
	         Transfer students who have not successfully completed college-level coursework in mathematics at their prior institution are required to take the Math Placement Test.  
	         </p>
	         
	         <p class="listing">
	         The exceptions to this policy are:
	         </p>
	         <ul class="listingBullet">
	            <li>Transfer students who receive a grade of 'C' or better in Intermediate Algebra at their transfer institution do not need to take the MPT. However, since Intermediate Algebra is not a 
	            college-level course, they will be required to provide proof of course completion in the form of a transcript to the Mathematics Department, in order to obtain permission to 
	            register for additional math courses at NEIU.</li>
	            <li>Transfer students who have submitted their ACT scores can be placed into a math course solely based on their ACT math score.</li>
	         </ul>
	         
	         <p>
	         For more information on math placement based on MPT results or ACT scores <a href="/access/NSFP/files/MathCourses-ACT.pdf" target="_blank">click here</a>.
	         </p>
	         
	         <br/>
	         
	         <h4>Preparing for the Math Placement Test: The Practice Test</h4>
	         
	         <p>
	         The Math Placement Test is a two-part exam where you get 30 minutes to finish each part. Since this is a once-a-year opportunity, students are 
	         strongly encouraged to review for the MPT. Click below to download practice tests (pdf files):
	         </p>
	         
	         <p>
             <b>Basic:</b> <a href="/access/NSFP/files/MPTPracticeElementaryAlgebra.pdf" target="_blank">Elementary Algebra</a> and <a href="/access/NSFP/files/MPTPracticeIntermediateAlgebra.pdf" target="_blank">Intermediate Algebra</a>
             </p>
             
             <p>
             <b>Advanced:</b> <a href="/access/NSFP/files/MPTPracticeIntermediateAlgebra.pdf" target="_blank">Intermediate Algebra</a> and <a href="/access/NSFP/files/MPTPracticeFunctionsGraphs.pdf" target="_blank">Pre-Calculus</a>
             </p>
             
             <p class="note">
             Please note: Calculators are not allowed to be used during MPT.
	        </p>
	        
