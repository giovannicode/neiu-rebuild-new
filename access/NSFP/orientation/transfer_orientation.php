<h5>When to Register</h5>

<p>You can register for Orientation at any time, but here are a few things to keep in mind:</p>

<ul class="listingBullet">
<li>Register at least two weeks (14 calendar days) in advance of your orientation date you wish to attend.  If you are requiting a meeting with an advisor during orientation, this will ensure your student file is prepared for academic advising.</li>
<li>Space is limited for each orientation date. The earlier you register, the more likely you are to receive the early orientation date of your choice.  
</li>
<li>The earlier you attend orientation, the more likely you are to get the classes you need at the days and times that you want.</li>
</ul>

<h5>How to Register</h5>

<p>In order to register for an Orientation session, you will need to:</p>
<ul class="listingBullet">
<li><strong>Know your Student ID Number</strong> - Each student is given a nine-digit identification number upon admissions, and it's listed on your admissions letter (Ex: 000123456).  If you can't find your letter, please call New Student and Family Programs at 772-443-5476.</li>
<li><strong>Know your e-mail address</strong> - Confirmation information will be sent to you electronically.</li>
<li><strong>Know any scheduling conflicts</strong> - Orientation is a one (1) day program; make sure you plan accordingly to attend the whole day. If you do not complete your orientation day, you may have to come back to attend another orientation session.</li>
