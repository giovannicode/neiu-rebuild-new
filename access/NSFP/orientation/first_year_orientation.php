<h5 class="show">Orientation Registration</h4>
<div class="hide">
<p>You may begin to register for your Orientation session date starting in April. Registration will be online, based on availability.  Once you register for an Orientation session, you will receive an email confirmation shortly thereafter.  We look forward to seeing you this summer.</p>

<h5>When to Register</h5>

<p>You can register for Orientation at any time, but here are a few things to keep in mind:</p>

<ul class="listingBullet">
<li>Make sure that you complete your placement test at least 3 weeks prior to the orientation date you select.  If your placement test is not completed prior to orientation you will need to reschedule after you have completed your placement tests.</li>
<li>Register at least two weeks (14 calendar days) in advance of the orientation date you wish to attend to ensure your student file is prepared for academic advising.</li>
<li>Space is limited for each orientation date. The earlier you register, the more likely you are to receive an early orientation date of your choice.</li>
<li>The earlier you attend orientation, the more likely you are to get the classes you need at the days and times that you want.</li>
