<p style="margin-left:35px"><strong>Student Support Services - </strong>Learn about many of the great services available to you to help you succeed at NEIU.  Difficulty with math? We have free tutoring available. Need to learn leadership skills? Get involved with student clubs and organizations. Need to write a winning resume? We can help you with that, too.</p>
