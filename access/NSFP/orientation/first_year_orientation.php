<h5 class="show">Advising at Orientation</h4>
<div class="hide">
<p>Advising is an educational process by which you are integrated into the University community. This is accomplished by assisting you in the transition from high school to college, providing accurate information about academic major requirements, promoting learning and utilization of academic and campus resources, and facilitating academic major and career exploration. During Orientation you will participate in an advising experience.  You will select your fall semester classes, learn about academic program requirements, academic policies and procedures, and other services that will help you achieve your academic success.</p>

<p>Enrollment in courses for Fall semester takes place during your Orientation session. Whether you attend the first or the last Orientation session, you will find a wide range of available courses, including courses that meet General Education, major and degree requirements. Because of the high demand for a particular session date, it is important to sign up for your Orientation session online as early as possible. All students attending Orientation will be able to find courses for the Fall Semester.</p>

