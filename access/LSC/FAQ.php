        <p class="question">What should I do before I see the tutor?</p>
           <ul class="questionBullet">
              <li>Put forth effort to attend the lectures and complete the readings and assignments for the course before you come to tutoring.</li>
              <li>Bring all books, notes, handouts and assignment sheets related to the course to the tutoring session.</li>
              <li>Come prepared with questions on the material that needs further exploration.</li>
              <li>Expect the tutor to assist further understanding of material but not to make up for any lack of effort on your part.</li>
              <li>If you realize that this subject presents a challenge for you, schedule a regular weekly appointment starting the first week of class.</li>
              <li>Together, tutor and student can develop a better understanding of the knowledge presented in the course and an improved approach to independent learning.</li>  
           </ul>
           
         <p class="question">What will a tutor not do for me?</p>
            <ul class="questionBullet">
               <li>A tutor is not able to do the course work, provide specific answers to test questions or complete an assignment. </li>
               <li>A tutor cannot be a substitute for a student's not attending class, not completing assigned readings, or not making an effort to understand the material in the course.</li>
            </ul>
	     
	     
	     <p class="question">What can I do to improve my study strategies?</p>
	        <ul class="questionBullet">
	            <li>Analyze the syllabus and reading schedules for each course and determine my reading/study rate for each text.</li>
	            <li>Create a study schedule, post it and follow it. </li>
	            <li>Schedule my semester, my week, and my day.</li>
	            <li>Use reading and study strategies, such as KWL and SQ3/4R.</li>
	            <li>Learn how to skim and scan text.</li>
	            <li>Highlight, take notes, and annotate text when I read.</li>
	            <li>Use a note taking system, such as the Cornell System, to combine my lecture notes and text notes to make connections.</li>
	            <li>Take time to review my notes and summarize them after each lecture.</li>
	            <li>Use mind maps when visualizing information.</li>
	            <li>Know the type of test that will be given in each course and create self tests, both objective and subjective.</li>
	            <li>Know how to choose and participate in a productive study group.</li>
	            <li>Visit the instructor during office hours.</li>
	            <li>Seek out the help of a tutor and attend sessions with questions.</li>
	        </ul>
	        
